package com.frys.bean;

public class CouponBean {
    
	private String mpid;
	private String pluLong;
	private String promotionCode;
	private String couponRequired;
	private String disValue;
	private String typeOfDis;
	private String startDate;
	private String endDate;
	
	
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getTypeOfDis() {
		return typeOfDis;
	}
	public void setTypeOfDis(String typeOfDis) {
		this.typeOfDis = typeOfDis;
	}
	public String getDisValue() {
		return disValue;
	}
	public void setDisValue(String disValue) {
		this.disValue = disValue;
	}
	public String getMpid() {
		return mpid;
	}
	public void setMpid(String mpid) {
		this.mpid = mpid;
	}
	public String getPluLong() {
		return pluLong;
	}
	public void setPluLong(String pluLong) {
		this.pluLong = pluLong;
	}
	public String getPromotionCode() {
		return promotionCode;
	}
	public void setPromotionCode(String promotionCode) {
		this.promotionCode = promotionCode;
	}
	public String getCouponRequired() {
		return couponRequired;
	}
	public void setCouponRequired(String couponRequired) {
		this.couponRequired = couponRequired;
	}
	
	
}
